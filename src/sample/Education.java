package sample;

public class Education {
    private String id;
    private String level;
    private String attestateNumber;
    private String attestateScore;
    private String specialityInfo;

    public Education() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getAttestateNumber() {
        return attestateNumber;
    }

    public void setAttestateNumber(String attestateNumber) {
        this.attestateNumber = attestateNumber;
    }

    public String getAttestateScore() {
        return attestateScore;
    }

    public void setAttestateScore(String attestateScore) {
        this.attestateScore = attestateScore;
    }

    public String getSpecialityInfo() {
        return specialityInfo;
    }

    public void setSpecialityInfo(String specialityInfo) {
        this.specialityInfo = specialityInfo;
    }
}
