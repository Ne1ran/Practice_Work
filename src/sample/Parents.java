package sample;

public class Parents {
    private String id;
    private String familyStatus;
    private String children;
    private String motherFio;
    private String motherAddress;
    private String motherNumber;
    private String motherWorkplace;
    private String fatherFio;
    private String fatherAddress;
    private String fatherNumber;
    private String fatherWorkplace;

    public Parents() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getFamilyStatus() {
        return familyStatus;
    }

    public void setFamilyStatus(String familyStatus) {
        this.familyStatus = familyStatus;
    }

    public String getChildren() {
        return children;
    }

    public void setChildren(String children) {
        this.children = children;
    }

    public String getMotherFio() {
        return motherFio;
    }

    public void setMotherFio(String motherFio) {
        this.motherFio = motherFio;
    }

    public String getMotherAddress() {
        return motherAddress;
    }

    public void setMotherAddress(String motherAddress) {
        this.motherAddress = motherAddress;
    }

    public String getMotherNumber() {
        return motherNumber;
    }

    public void setMotherNumber(String motherNumber) {
        this.motherNumber = motherNumber;
    }

    public String getMotherWorkplace() {
        return motherWorkplace;
    }

    public void setMotherWorkplace(String motherWorkplace) {
        this.motherWorkplace = motherWorkplace;
    }

    public String getFatherFio() {
        return fatherFio;
    }

    public void setFatherFio(String fatherFio) {
        this.fatherFio = fatherFio;
    }

    public String getFatherAddress() {
        return fatherAddress;
    }

    public void setFatherAddress(String fatherAddress) {
        this.fatherAddress = fatherAddress;
    }

    public String getFatherNumber() {
        return fatherNumber;
    }

    public void setFatherNumber(String fatherNumber) {
        this.fatherNumber = fatherNumber;
    }

    public String getFatherWorkplace() {
        return fatherWorkplace;
    }

    public void setFatherWorkplace(String fatherWorkplace) {
        this.fatherWorkplace = fatherWorkplace;
    }
}
